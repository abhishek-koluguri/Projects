var totalValues = function(values) {
  var total = 0;
  for(var i = 0; i < values.length; i++) {
    total += values[i];
  }
  return total;
}

var totalEvenValues = function(values) {
  var total = 0;
  for(var i = 0; i < values.length; i++) {
    if(values[i] % 2 == 0)
      total += values[i];
  }
  return total;
}

var totaloddValues = function(values) {
  var total = 0;
  for(var i = 0; i < values.length; i++) {
    if(values[i] % 2 != 0)
      total += values[i];
  }
  return total;
}

console.log(totalValues([1, 2, 3, 4, 5, 6]));
console.log(totalEvenValues([1, 2, 3, 4, 5, 6]));
console.log(totaloddValues([1, 2, 3, 4, 5, 6]));
