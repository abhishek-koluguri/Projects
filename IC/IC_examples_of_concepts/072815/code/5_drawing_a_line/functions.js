var drawOn = function(canvas) {
  var center = {
    x: canvas.width / 2,
    y: canvas.height / 2
  };

  var context = canvas.getContext('2d');
  var displacement = 50;
  context.moveTo(center.x - displacement, center.y);
  context.lineTo(center.x + displacement, center.y);
  context.strokeStyle = 'blue';
  context.lineWidth = 5;
  context.stroke();
}